class Product {
  final int pId;
  final String pName;
  final String pType;
  final String pImage;
  final String pPrice;
  final String pOldPrice;
  final String pDescription;
  final String pQuantity;

  Product({
    required this.pId,
    required this.pName,
    required this.pType,
    required this.pImage,
    required this.pPrice,
    required this.pOldPrice,
    required this.pDescription,
    required this.pQuantity,
  });
}
